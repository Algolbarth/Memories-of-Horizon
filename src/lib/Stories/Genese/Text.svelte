Au début il n'y avait rien.

<br />
<br />

Puis la lumière et les ténèbres se sont partagé le monde, la première dominant
les cieux et les seconds tapissant les profondeurs.

<br />
<br />

Ensuite l'eau recouvra le monde et laissa l'air s'installer au-dessus d'elle.

<br />
<br />

Dans l'eau émergea alors la terre chatouillant l'air tandis que le feu se
regroupa sous forme de boule au-dessus de ces îles nouvellement créées.

<br />
<br />

Pour que ces éléments puissent agir entre eux, la foudre leur incita le
mouvement et la glace limita ce dernier pour garantir un équilibre des forces.

<br />
<br />

Une fois que le changement et la continuité arrivèrent dans l'univers, la vie
put prendre son essor et les végétaux poussèrent partout où ils pouvaient.

<br />
<br />

Mais la vie implique la mort et cette dernière emporta les êtres vivants trop
faibles, ne laissant que les plus robustes pour dévellopper la faune et la
flore.

<br />
<br />

Enfin la vie donna naissance à la conscience, et avec elle les croyances de
l'arcane et les sciences des métaux.
