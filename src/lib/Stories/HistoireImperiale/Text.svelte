</p>
<p>
	Le calendrier impérial fixe son année 0 sur la fondation de la capitale, considéré comme la
	création officielle de l'Empire. Les chroniques de l'Empire consignent les événements les plus
	marquants de l'histoire connue.
</p>
<p>
	Le commerce amorcé par les factions humaines pour réunir les continents fut fructueux, mais la
	diversité des guildes marchandes mena à la création de nombreuses monnaies. Ce faisant, les
	continents ne partageaient toujours pas de monnaie commune et étaient tout aussi distant
	qu'auparavant. Avec l'instauration d'une monnaie impériale, la couronne impériale, tous les
	peuples étaient unis économiquement malgré la distance.
</p>
