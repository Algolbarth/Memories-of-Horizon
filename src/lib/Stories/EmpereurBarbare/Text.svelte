Si les empereurs ont l'habitude de se faire discret pour incarner au mieux un pouvoir dépassant les citoyens et proche d'un dieu, ce n'est pas le cas de tous les empereurs qui ont traversé l'histoire.

<br />
<br />

En particulier pour celui qui fut surnommé l'Empereur barbare, en raison de son origine. Bruyant, extraverti et violent, cet Empereur déstabilisa le pouvoir en place et choqua le siège par sa manière de faire, en bien comme en mal.

<br />
<br />

Il restera dans l'histoire pour ses colères, son goût du combat et ses nombreuses apparitions en public faisant de lui l'Empereur le plus controversé de tous les temps.

<br />
<br />

Beaucoup voyaient en lui un héritier illégitime, incapable de veiller sur l'Empire et de remplir ses responsabilités.

<br />
<br />

Malgré les questions que soulevaient son attitude, il se montra à la hauteur de son titre en gagnant le respect des peuples les plus combattifs et en ralliant à l'Empire des camps considérés comme nuisibles.

<br />
<br />

Contrairement aux autres empereurs, il considérait ses plus proches conseillers comme de véritables compagnons d'armes et non seulement comme de futur héritiers à tester.

<br />
<br />

Sa combativité et son courage le poussèrent au combat en première ligne, indignant le siège impérial qui considérait cela comme de l'inconscience mais galvanisait les troupes.

<br />
<br />

Ces dernières ne se sentirent pas abandonné au combat par un chef lâche et inaccessible, mais mené par un chef de guerre compétent et invincible.

<br />
<br />

Fidèle à lui même jusqu'à sa mort, il tombera au combat contre un archidémon lors de la bataille contre les démons.